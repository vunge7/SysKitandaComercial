        public boolean validar_numero_menor(){
    //        if(txtValorTranferencia.getText() != null){
            if(Double.parseDouble(txtValorSair.getText().trim() ) == 0){
                JOptionPane.showMessageDialog(null, "Atenção\nInsira o valor a Saír do Caixa!");
                txtValorSair.requestFocus();
                return false;
            }return true;

        }
            
        public boolean validar_descricao(){
    //        if(txtValorTranferencia.getText() != null){
            if(taDescricao.getText().equals("") ){
                JOptionPane.showMessageDialog(null, "Atenção\nDigite a descrição da Saída do Valor!");
                taDescricao.requestFocus();
                return false;
            }return true;

        }

        public boolean validar_valor_saldo(){
           
           TbBanco banco = bancoDao.findTbBanco(3);
            
//           valor = banco.getSaldoBanco() - valor;
            
            if((Double.parseDouble(txtValorSair.getText())) > banco.getSaldoBanco()){
                JOptionPane.showMessageDialog(null, "Atenção\nO valor a Sair não pode ser superior ao saldo do Caixa!");
                return false;
            }return true;
        }
        
        private void procedimento_salvar(){
        
            if (validar_numero_menor()) {

                if (validar_descricao()) {

                    if (validar_valor_saldo()) {

                        try {
                            saidasTesouraria = new SaidasTesouraria();
                            setDadosSaidas();
                            saidaDao.create(saidasTesouraria);
                            MetodosUtil.subtrair_saldo_banco(saidasTesouraria.getValor(), DVML.COD_BANCO_CAIXA, conexao  );
                            JOptionPane.showMessageDialog(null, "Saída efectuada com sucesso!...");
                            limparDados();
                        } catch (Exception e) {
                            JOptionPane.showMessageDialog(null, "Erro ao dar saída ", "ERRO", JOptionPane.ERROR_MESSAGE);
                        }

                    }

                                    
                }
                                    
                                    
            }                        
              
    }
    
    public void setDadosSaidas(){
        
        saidasTesouraria.setValor( Double.parseDouble(txtValorSair.getText()));
        saidasTesouraria.setDescricao(  taDescricao.getText());
        saidasTesouraria.setDataSaida(new Date() );
        saidasTesouraria.setHoraSaida(new Date() );
        saidasTesouraria.setFkUsuario(usuarioDao.findTbUsuario(idUser));
           
    }
    
    private void limparDados(){
        
        txtValorSair.setText("0");
        taDescricao.setText("");
        
    }
    
    
    
