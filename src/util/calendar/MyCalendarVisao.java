    private void procedimento_clique_button( MouseEvent evt, JPanel painel, JButton button )
    {

        if ( evt.getClickCount() >= 2 )
        {
            clique_duplo_button( painel );
            Export export = new Export().getExportByPanel( painel, export_list );
            if ( export != null )
            {
                export_list.remove( export );
            }
        }
        else if ( evt.getClickCount() == 1 )
        {
            clique_button( painel );

            if ( !new Export().exist_export( painel, export_list ) )
            {
                Export export = new Export();
                export.setButton( button );
                export.setPainel( painel );

                String dataString = getAno() + "-" + getIdMes() + "-" + button.getText();
                Date data_seleccionada = MetodosUtil.stringToDate( dataString, "yyyy-MM-dd" );

                export.setDate( data_seleccionada );

                export_list.add( export );
            }

        }
    }

    private List<Date> getDataSeleccionadas()
    {
        List<Date> list_date = new ArrayList<>();

        for ( int i = 0; i < export_list.size(); i++ )
        {
            list_date.add( export_list.get( i ).getDate() );
            System.out.println( "Datas Selecionadas: " + MetodosUtil.getDataBanco( export_list.get( i ).getDate() ) );
        }

        dispose();
        return list_date;
    }

    private void adicionar_falta()
    {

        ExtraSalarioFuncionarioVisao.procedimento_registrar_falta( getDataSeleccionadas() );
        dispose();

    }

    class Export
    {

        Date date;
        JPanel painel;
        JButton button;

        public Export()
        {
        }

        public Date getDate()
        {
            return date;
        }

        public void setDate( Date date )
        {
            this.date = date;
        }

        public JPanel getPainel()
        {
            return painel;
        }

        public void setPainel( JPanel painel )
        {
            this.painel = painel;
        }

        public JButton getButton()
        {
            return button;
        }

        public void setButton( JButton button )
        {
            this.button = button;
        }

        public Export getExportByPanel( JPanel panel, List<Export> lista )
        {
            for ( Export export : lista )
            {
                // if ( export.getPainel().equals( panel ) && export.getPainel().getBackground().equals( clique) )
                if ( export.getPainel().equals( panel ) )
                {
                    return export;
                }
            }

            return null;

        }

        public boolean exist_export( JPanel panel, List<Export> lista )
        {
            for ( Export export : lista )
            {
                // if ( export.getPainel().equals( panel ) && export.getPainel().getBackground().equals( clique) )
                if ( export.getPainel().equals( panel ) )
                {
                    return true;
                }
            }

            return false;

        }

    }

