    private void adicionar()
    {
          DefaultTableModel modelo = (DefaultTableModel)  tabela_categoria.getModel();          
          List<TbTipoProduto> list = tipoProdutoDao.getAll();
          
          
          
          modelo.setRowCount(0);
          for (int i = 0; i < list.size(); i++) {
              
              TbTipoProduto tipoProduto = list.get(i);            
              modelo.addRow(     
                     new Object[]
                             {
                                   tipoProduto.getCodigo(),
                                   tipoProduto.getDesignacao()          
                            }
                  );
        }
    
    }

    
    private void setDados()
    {
               
          this.tipoProduto.setDesignacao(  txtTipoProduto.getText() );
    
    }
    
    
    private void procedimento_salvar()
    {
            
          if ( valido() ) {
              
              
              if (  !tipoProdutoDao.exist(  txtTipoProduto.getText()  )   ) {
                  
                  this.tipoProduto = new TbTipoProduto();
                  setDados();
                  try {
                      tipoProdutoDao.create(tipoProduto);
                      limpar();
                      adicionar();
                      JOptionPane.showMessageDialog(null, "Categoria salva com sucesso!...", DVML.DVML_COMERCIAL, JOptionPane.INFORMATION_MESSAGE);
                  } catch (Exception e) {
                      JOptionPane.showMessageDialog(null, "Erro ao salvar a categoria", DVML.DVML_COMERCIAL, JOptionPane.ERROR_MESSAGE);
                  }

              }else JOptionPane.showMessageDialog(null, "Já existe uma categoria com esta designação", DVML.DVML_COMERCIAL, JOptionPane.WARNING_MESSAGE);
              
              
            
          }
 
    
    
    }
    
    
    
      
    private void procedimento_alterar()
    {
            
          if ( valido() ) {
              
                  this.tipoProduto = tipoProdutoDao.findTbTipoProduto(this.codigo );
                  setDados();
                  try {
                      tipoProdutoDao.edit(tipoProduto);
                      limpar();
                      adicionar();
                      JOptionPane.showMessageDialog(null, "Categoria alterada com sucesso!...", DVML.DVML_COMERCIAL, JOptionPane.INFORMATION_MESSAGE);
                  } catch (Exception e) {
                      JOptionPane.showMessageDialog(null, "Erro ao alteraer a categoria", DVML.DVML_COMERCIAL, JOptionPane.ERROR_MESSAGE);
                  }

            
            
          }
 
    
    
    }
    
    
    
    private void procedimento_eliminar()
    {
            int opcao = JOptionPane.showConfirmDialog(null, "Tens a plena certeza que pretendes eliminar essa categria?");
            
            if ( opcao == JOptionPane.YES_OPTION  ) {
                
                try {
                    
                    tipoProdutoDao.destroy(this.codigo);
                    adicionar();
                    limpar();
                    JOptionPane.showMessageDialog(null, "Categoria eliminada com sucesso!...", DVML.DVML_COMERCIAL, JOptionPane.INFORMATION_MESSAGE);
                    
                } catch (Exception e) {
                    
                    e.printStackTrace();
                    JOptionPane.showMessageDialog(null, "Erro ao eliminar essa categoria.\nPossivelmente já esta realcionada com alguns produtos", DVML.DVML_COMERCIAL, JOptionPane.ERROR_MESSAGE);
                }
                
            }else JOptionPane.showMessageDialog(null, "Categoria não eliminada", DVML.DVML_COMERCIAL, JOptionPane.INFORMATION_MESSAGE);
            
        
    
    
    }
    
    
    
    
    
    
    
    
    private boolean valido()
    {
            
           if (txtTipoProduto.equals("") ) {
               
               txtTipoProduto.requestFocus();
               JOptionPane.showMessageDialog(null, "Pf. digite a categoria", DVML.DVML_COMERCIAL, JOptionPane.WARNING_MESSAGE);
               return false;
            
            }
           
           return true;
    
    }
    
    
    
 
    private void mostar_dados()
    {
                DefaultTableModel modelo  = (DefaultTableModel) tabela_categoria.getModel();
               
                
                this.codigo = Integer.parseInt(   modelo.getValueAt(   tabela_categoria.getSelectedRow()   , 0).toString()   );
                this.tipoProduto = tipoProdutoDao.findTbTipoProduto(this.codigo);
                txtTipoProduto.setText(  this.tipoProduto.getDesignacao()   );
               
    
    
    }
            
    
    
    
    
    
