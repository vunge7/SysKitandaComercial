    private boolean campos_validos()
    {

        String qtd_acerto = txt_qtd_acerto.getText();
        String motivo = txtaMotivo.getText();

        if ( motivo.equals( "" ) )
        {
            JOptionPane.showMessageDialog( null, "Por favor, mencione o motivo do acerto de stock", "AVISO", JOptionPane.WARNING_MESSAGE );
            txtaMotivo.requestFocus();
            return false;
        }else
        if ( qtd_acerto.equals( "" ) )
        {
            JOptionPane.showMessageDialog( null, "Por favor define a quantidade a acertar no stock", "AVISO", JOptionPane.WARNING_MESSAGE );
            return false;
        }
        else
        {
            try
            {
                this.qtd = Integer.parseInt( qtd_acerto );

            }
            catch ( Exception e )
            {
                JOptionPane.showMessageDialog( null, "Tem quer ser um n√∫mero", "AVISO", JOptionPane.WARNING_MESSAGE );
                return false;
            }
        }

        return true;
    }

    private void procedimento_actualizar()
    {
        if ( campos_validos() )
        {
            if ( possivel_actualizar() )
            {
                try
                {
                    conexaoTransaction = BDConexao.getInstancia();
                    DocumentoDao.startTransaction( conexaoTransaction );
                    double qtdAnterior = stoksController.getQuantidadeProduto( cod_produto, cod_armazem );
                    StoksController stoksControllerLocal = new StoksController( conexaoTransaction );

                    if ( stoksController.existe_stock( cod_produto, cod_armazem ) )
                    {
                        boolean adicionou = stoksControllerLocal.adicionar_quantidades( cod_produto, qtd, cod_armazem );
                        if ( adicionou )
                        {
                            if ( registrarAcertoStock( qtdAnterior, qtd ) )
                            {
                                DocumentoDao.commitTransaction( conexaoTransaction );
                                AcertoStockVisao.mostrar_qtd_existente( cod_produto, cod_armazem, conexaoTransaction );
                                conexaoTransaction.close();
                                JOptionPane.showMessageDialog( null, "Produto actualizado com sucesso!...", "INFO", JOptionPane.INFORMATION_MESSAGE );
                                dispose();
                            }
                            else
                            {
                                DocumentoDao.rollBackTransaction( conexaoTransaction );
                                conexaoTransaction.close();
                                JOptionPane.showMessageDialog( null, "Falha ao actualizar no stock", "ERROR", JOptionPane.ERROR_MESSAGE );

                            }

                        }

                    }
                    else
                    {
                        DocumentoDao.rollBackTransaction( conexaoTransaction );
                        conexaoTransaction.close();
                        JOptionPane.showMessageDialog( null, "Produto n√£o existe no stock seleccionado.", "ERROR", JOptionPane.ERROR_MESSAGE );
                    }

                }
                catch ( Exception e )
                {
                    DocumentoDao.rollBackTransaction( conexaoTransaction );
                    conexaoTransaction.close();
                    JOptionPane.showMessageDialog( null, "Falha ao actualizar no stock", "ERROR", JOptionPane.ERROR_MESSAGE );
                }
            }
            else
            {
                JOptionPane.showMessageDialog( null, "Impossivel realizar o acerto de stock", "ERROR", JOptionPane.ERROR_MESSAGE );
            }
        }
    }

    private void setDados()
    {
        produto = ( TbProduto ) produtosController.findById( cod_produto );
        lb_nome_produto.setText( produto.getDesignacao() );
        txt_qtd.setText( String.valueOf( conexao.getQtdExistenteStock( cod_produto, cod_armazem ) ) );
    }

    private boolean possivel_actualizar()
    {
        return ( ( qtd + conexao.getQtdExistenteStock( cod_produto, cod_armazem ) ) >= 0 );
    }

    private static boolean registrarAcertoStock( double qtdAnterior, double qtd )
    {
        AcertoStock acertoStock = new AcertoStock();
        TbProduto produtoLocal = ( TbProduto ) produtosController.findById( cod_produto );
        TbArmazem armazem = ( TbArmazem ) armazensController.findByCodigo( cod_armazem );

        TbUsuario usuario = ( TbUsuario ) usuariosController.findById( idUser );

        double qtdAcerto = qtd;
        double qtdDepois = qtdAnterior + qtd;
        int codProduto = cod_produto;
        String designacaoProduto = produtoLocal.getDesignacao();
        int codArmazem = cod_armazem;
        String desinacaoArmazem = armazem.getDesignacao();
        String nomeUsuario = usuario.getNome();

        acertoStock.setDataHora( new Date() );
        acertoStock.setCodProduto( codProduto );
        acertoStock.setDesignacaoProduto( designacaoProduto );
        acertoStock.setCodUsuario( idUser );
        acertoStock.setDesigncaoArmazem( desinacaoArmazem );
        acertoStock.setQtdAnterior( qtdAnterior );
        acertoStock.setQtdAcerto( qtdAcerto );
        acertoStock.setQtdDepois( qtdDepois );
        acertoStock.setNomeUsuario( nomeUsuario );
        acertoStock.setCodArmazem( codArmazem );
        acertoStock.setMotivoAcerto(txtaMotivo.getText() );

        return acertosStockController.salvar( acertoStock );

    }

