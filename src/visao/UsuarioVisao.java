    public void operacao( int operacao, String descricaoCerto, String descricaoErro )
    {
        try
        {
            String novaSenha = novaSenha();

            //int codigo_sexo, int idTipousuario, String nome ,String senha, String status, String dataNascimento, String telefone, String email, String endereco, String userName
            usuarioModelo = new UsuarioModelo( codigo, getSexo(), getIdTipoUsuario(), getNome(),
                    novaSenha,
                    getStatus(),
                    getDataNascimento(),
                    getTelefone(),
                    getEmail(),
                    getEndereco(),
                    getUserName()
            );

            TbUsuario tbUsuario = null;

            if ( operacao != 3 )
            {
                if ( !campos_invalidos() )
                {
                    tbUsuario = usuariosController.createNewUsuarioInstance( operacao, usuarioModelo );
                    System.err.println( "tbUsuario: " + tbUsuario );
                    if ( tbUsuario != null )
                    {
                        JOptionPane.showMessageDialog( null, "DADOS   " + descricaoCerto + " COM SUCESSO NA BD!..." );
                        limpar();
                    }
                    else
                    {
                        System.err.println( "#1 operacao != 3 tbUsuario: " + tbUsuario );
                        JOptionPane.showMessageDialog( null, "ERRO AO " + descricaoErro + "NA BD!...", "ERRO", JOptionPane.ERROR_MESSAGE );
                    }

                }
            }
            else
            {
                if ( usuariosController.operacao( operacao, usuarioModelo ) )
                {
                    JOptionPane.showMessageDialog( null, "DADOS   " + descricaoCerto + " COM SUCESSO NA BD!..." );
                    limpar();
                }
                else
                {
                    System.err.println( "#1 else tbUsuario: " + tbUsuario );
                    JOptionPane.showMessageDialog( null, "ERRO AO " + descricaoErro + "NA BD!...", "ERRO", JOptionPane.ERROR_MESSAGE );
                }
            }

            revelarSenha( tbUsuario.getCodigo(), novaSenha );
        }
        catch ( SQLException ex )
        {
            Logger.getLogger( UsuarioVisao.class.getName() ).log( Level.SEVERE, null, ex );
        }

    }

