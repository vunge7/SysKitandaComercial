    private void revelarSenha( int codigoUtilizador, String novasenha )
    {
//        BDConexao conexao = new BDConexao();

        Connection connection = ( Connection ) conexao.conectar();
        HashMap hashMap = new HashMap();
        String relatorio = "";
        relatorio = "relatorios/credenciais.jasper";
        System.err.println( "codigoUtilizador: " + codigoUtilizador );
        hashMap.put( "_UTILIZADOR", codigoUtilizador );
        hashMap.put( "_SENHA_DESINCRIPTADA", novasenha );

        File file = new File( relatorio ).getAbsoluteFile();
        String obterCaminho = file.getAbsolutePath();

        try
        {

            JasperFillManager.fillReport( obterCaminho, hashMap, connection );

            JasperPrint jasperPrint = JasperFillManager.fillReport( obterCaminho, hashMap, connection );

            if ( jasperPrint.getPages().size() >= 1 )
            {
                JasperViewer jasperViewer = new JasperViewer( jasperPrint, false );

                jasperViewer.setVisible( true );
            }
            else
            {
                JOptionPane.showMessageDialog( null, "Nao Existem Usuarios Para essa Listagen!..." );
            }
        }
        catch ( JRException jex )
        {
            jex.printStackTrace();
            //System.out.println("aqui");
            JOptionPane.showMessageDialog( null, "FALHA AO TENTAR MOSTRAR OS USUARIOS!..." );
        }
        catch ( Exception ex )
        {
            ex.printStackTrace();
            JOptionPane.showMessageDialog( null, "ERRO AO TENTAR OS USUARIOS!..." );
        }
    }

