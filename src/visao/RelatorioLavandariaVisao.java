    private void adicionar_tipo_de_servicos()
    {
        try
        {

            List<TbTipoProduto> tipo_servicos = tipoProdutoDao.buscaTodasCategoriasProdutos();
            lista_tipo_servicos_local.clear();
            for ( int i = 0; i < tipo_servicos.size(); i++ )
            {
                lista_tipo_servicos_local.addElement( tipo_servicos.get( i ).getDesignacao() );
            }

        }
        catch ( Exception e )
        {
        }

    }

