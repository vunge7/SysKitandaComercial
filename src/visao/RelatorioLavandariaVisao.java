    private void procedimento_imprimir()
    {

        List<ProdutosUtil> lista_local = new ArrayList<>();
        ProdutosUtil produtosUtil;
        List<TbTipoProduto> lista_tipo_produto = new TipoProdutoDao( emf ).buscaTodasCategoriasProdutos();

        for ( int i = 0; i < lista_tipo_produto.size(); i++ )
        {

            List<TbStock> stock = new StockDao( emf ).getProdutosByTipo( lista_tipo_produto.get( i ).getDesignacao() );

            if ( !stock.isEmpty() )
            {

                produtosUtil = new ProdutosUtil();
                produtosUtil.setDesignacao( lista_tipo_produto.get( i ).getDesignacao().toUpperCase() );
                produtosUtil.setCusto( "" );
                produtosUtil.setQtd( "" );
                produtosUtil.setMoeda( "" );
                lista_local.add( produtosUtil );

                for ( int j = 0; j < stock.size(); j++ )
                {
                    produtosUtil = new ProdutosUtil();
                    produtosUtil.setCod_servico( String.valueOf( stock.get( j ).getCodProdutoCodigo().getCodigo() ) );
                    produtosUtil.setDesignacao( stock.get( j ).getCodProdutoCodigo().getDesignacao() );
                    produtosUtil.setCusto( String.valueOf( stock.get( j ).getPrecoVenda() ) );
                    produtosUtil.setQtd( String.valueOf( stock.get( j ).getQuantidadeExistente() ) );
                    produtosUtil.setMoeda( "AKZ" );
                    lista_local.add( produtosUtil );
                }

                produtosUtil = new ProdutosUtil();
                produtosUtil.setCod_servico( "" );
                produtosUtil.setCusto( "" );
                produtosUtil.setQtd( "" );
                produtosUtil.setDesignacao( "" );
                produtosUtil.setMoeda( "" );
                lista_local.add( produtosUtil );

            }
        }

        new Produtos( lista_local, "" );

    }

