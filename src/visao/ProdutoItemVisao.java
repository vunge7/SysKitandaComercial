    private void procedimentoEdicionar()
    {
        if ( formulario.equals( DVML.FORMULARIO_PEDIDO_RESTAURANTE ) )
        {
            NovaGestaoPedidosVisao.adicionarImportar( designacao );
        }
        if ( formulario.equals( DVML.FORMULARIO_VENDA_EXPRESSO ) )
        {
            VendasPraticasVisao.adicionarImportar( designacao );
        }
        if ( formulario.equals( DVML.FORMULARIO_RECOLHA_LAVANDARIA ) )
        {
            RecolhaPedidosVisao.adicionarImportar( designacao );
        }
    }

    private void visualizarQtd()
    {
        if ( idTipoUsuario == 1 )
        {
            lbQtd.setVisible( true );
        }
        else
        {
            lbQtd.setVisible( false );
        }
    }

