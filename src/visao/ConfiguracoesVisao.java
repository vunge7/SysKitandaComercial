    private Date getData( JComboBox cmbHora, JComboBox cmbMinuto )
    {
        Date data = new Date();
        int hora = Integer.parseInt( cmbHora.getSelectedItem().toString() );
        int minuto = Integer.parseInt( cmbMinuto.getSelectedItem().toString() );
        data.setHours( hora );
        data.setMinutes( minuto );
        data.setSeconds( 0 );
        return data;

    }

    private boolean validarHora( JComboBox cmbHoraComeco, JComboBox cmbTermino )
    {

        int horaComeco = Integer.parseInt( cmbHoraComeco.getSelectedItem().toString() );
        int horaTermino = Integer.parseInt( cmbTermino.getSelectedItem().toString() );

        if ( horaComeco >= horaTermino )
        {
            JOptionPane.showMessageDialog( null, "A hora do começo não pode ser maior ou igual que a hora do término." );
            return false;
        }

        return true;

    }

