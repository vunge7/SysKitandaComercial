    private static void busca()
    {
        if ( Objects.nonNull( produtoLocal ) )
        {
            lbArtigo.setText( produtoLocal.getDesignacao() );

            double quantidadeProduto = stocksController.getQuantidadeProduto( produtoLocal.getCodigo(), getIdArmazem() );
            lbQtd.setText( String.valueOf( quantidadeProduto ) );
            Calendar cal = Calendar.getInstance();

// Data Início -> 00:00:00
            cal.setTime( dcDataInicio.getDate() );
            cal.set( Calendar.HOUR_OF_DAY, 0 );
            cal.set( Calendar.MINUTE, 0 );
            cal.set( Calendar.SECOND, 0 );
            cal.set( Calendar.MILLISECOND, 0 );
            Date dataInicio = cal.getTime();

// Data Fim -> 23:59:59
            cal.setTime( dcDataFim.getDate() );
            cal.set( Calendar.HOUR_OF_DAY, 23 );
            cal.set( Calendar.MINUTE, 59 );
            cal.set( Calendar.SECOND, 59 );
            cal.set( Calendar.MILLISECOND, 999 );
            Date dataFim = cal.getTime();

            carregarTabelaMovimentacoes( produtoLocal.getCodigo(),
                    getIdArmazem(), dataInicio,
                    dataFim );
        }
        else
        {
            lbArtigo.setText( "" );
        }
    }

    private void procedimentoVisualizacao()
    {

        HashMap hashMap = new HashMap();

        if ( ckArtigo.isSelected() )
        {
            hashMap.put( "DATA_01", dcDataInicio.getDate() );
            hashMap.put( "DATA_02", dcDataFim.getDate() );
            hashMap.put( "ID_PRODUTO", produtoLocal.getCodigo() );
            hashMap.put( "ID_ARMAZEM", getIdArmazem() );
            String fileName = "cardex.jasper";
            AnyReport anyReport = new AnyReport( hashMap, fileName );

        }
        else
        {
            hashMap.put( "DATA_STOCK", dcDataInicio.getDate() );
            hashMap.put( "ID_ARMAZEM", getIdArmazem() );
            String fileName = "cardex_geral.jasper";
            AnyReport anyReport = new AnyReport( hashMap, fileName );
        }

    }

