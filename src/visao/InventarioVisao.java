    private void adicionar_tipo_de_servicos()
    {
        try
        {

            List<TbTipoProduto> tipo_servicos = tipoProdutoDao.buscaTodasCategoriasProdutos();
            lista_tipo_servicos_local.clear();
            for ( int i = 0; i < tipo_servicos.size(); i++ )
            {
                lista_tipo_servicos_local.addElement( tipo_servicos.get( i ).getDesignacao() );
            }

        }
        catch ( Exception e )
        {
        }

    }

    private void adicionar_servicos_by_tipo_de_servico( String designacao )
    {
        try
        {

            DefaultTableModel modelo = ( DefaultTableModel ) tb_servicos.getModel();
            List<TbProduto> produtos = produtoDao.getProdutosByTipo( designacao );

            //Limpa a Tabela
            modelo.setRowCount( 0 );
            System.out.println( "TAMANHO LISTA : " + produtos.size() );
            for ( int i = 0; i < produtos.size(); i++ )
            {

                modelo.addRow( new Object[]
                {
                    produtos.get( i ).getCodigo(),
                    produtos.get( i ).getDesignacao(),
                    stockDao.getStockByDescricao( produtos.get( i ).getCodigo(), getIdArmazem() ).getQuantidadeExistente(),
                    stockDao.getStockByDescricao( produtos.get( i ).getCodigo(), getIdArmazem()
                    ).getPrecoVenda(),
                    "AKZ"
                } );

            }

        }
        catch ( Exception e )
        {
        }

    }

    public void mostrar_servicos()
    {

        BDConexao conexao = new BDConexao();

        Connection connection = ( Connection ) conexao.conectar();
        HashMap hashMap = new HashMap();
        String relatorio = "";
        relatorio = "relatorios/Produto_Stock.jasper";

        File file = new File( relatorio ).getAbsoluteFile();
        String obterCaminho = file.getAbsolutePath();

        try
        {

            JasperFillManager.fillReport( obterCaminho, hashMap, connection );

            JasperPrint jasperPrint = JasperFillManager.fillReport( obterCaminho, hashMap, connection );

            if ( jasperPrint.getPages().size() >= 1 )
            {
                JasperViewer jasperViewer = new JasperViewer( jasperPrint, false );

                jasperViewer.setVisible( true );
            }
            else
            {
                JOptionPane.showMessageDialog( null, "Nao Existem Usuarios Para essa Listagen!..." );
            }
        }
        catch ( JRException jex )
        {
            jex.printStackTrace();
            //System.out.println("aqui");
            JOptionPane.showMessageDialog( null, "FALHA AO TENTAR MOSTRAR OS USUARIOS!..." );
        }
        catch ( Exception ex )
        {
            ex.printStackTrace();
            JOptionPane.showMessageDialog( null, "ERRO AO TENTAR OS USUARIOS!..." );
        }

    }

    private void procedimento_imprimir()
    {

        List<ProdutosUtil> lista_local = new ArrayList<>();
        ProdutosUtil produtosUtil;
        List<TbTipoProduto> lista_tipo_produto = new TipoProdutoDao( emf ).buscaTodasCategoriasProdutos();

        for ( int i = 0; i < lista_tipo_produto.size(); i++ )
        {

            List<TbStock> stock = new StockDao( emf ).getProdutosByTipo( lista_tipo_produto.get( i ).getDesignacao() );

            if ( !stock.isEmpty() )
            {

                produtosUtil = new ProdutosUtil();
                produtosUtil.setDesignacao( lista_tipo_produto.get( i ).getDesignacao().toUpperCase() );
                produtosUtil.setCusto( "" );
                produtosUtil.setQtd( "" );
                produtosUtil.setMoeda( "" );
                lista_local.add( produtosUtil );

                for ( int j = 0; j < stock.size(); j++ )
                {
                    produtosUtil = new ProdutosUtil();
                    produtosUtil.setCod_servico( String.valueOf( stock.get( j ).getCodProdutoCodigo().getCodigo() ) );
                    produtosUtil.setDesignacao( stock.get( j ).getCodProdutoCodigo().getDesignacao() );
                    produtosUtil.setCusto( String.valueOf( stock.get( j ).getPrecoVenda() ) );
                    produtosUtil.setQtd( String.valueOf( stock.get( j ).getQuantidadeExistente() ) );
                    produtosUtil.setMoeda( "AKZ" );
                    lista_local.add( produtosUtil );
                }

                produtosUtil = new ProdutosUtil();
                produtosUtil.setCod_servico( "" );
                produtosUtil.setCusto( "" );
                produtosUtil.setQtd( "" );
                produtosUtil.setDesignacao( "" );
                produtosUtil.setMoeda( "" );
                lista_local.add( produtosUtil );

            }
        }

        new Produtos( lista_local, "" );

    }

