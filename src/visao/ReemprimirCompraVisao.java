    private void procedimento_reimprimir()
    {

        String ref_doc = txtFactura.getText();
        compras = comprasDao.findByCodFactReemprensao( ref_doc );

        if ( !Objects.isNull( compras ) )
        {
            Abreviacao abreviacao = DVML.getAbreviacao( compras.getFkDocumento().getPkDocumento() );
            //para casos de facturas simplificadas ou facturas A4.
//            abreviacao = rb_A6.isSelected() ? 
//            DVML.Abreviacao.CO:abreviacao;
            List<TbProduto> lista_produto_isentos = new ArrayList<>();
            lista_produto_isentos = MetodosUtil.getProdutosIsentosCompras( compras.getItemComprasList() );
            String motivos_isentos = MetodosUtil.getMotivoIsensaoProdutos( lista_produto_isentos );
//            ListaVenda1 listaVenda1 = new ListaVenda1( compras.getCodigo(), abreviacao, false, false, SEGUNDA_VIA_CONFORMIDADE_COM_ORIGINAL, motivos_isentos );
            exibirReciboDeCompras();
        }
        else
        {
            compras = null;
            JOptionPane.showMessageDialog( null, "Documento não existente na base de dados. \nObs:Verique a referência. " );
        }

    }

    private void exibirReciboDeCompras()
    {
        try
        {

            Connection connection = (Connection) BDConexao.getConexao();
            HashMap hashMap = new HashMap();
            //this.motivo_isencao = "Regime Transitório";
//        hashMap.put( "CODIGO_VENDA", this.codigo );
//        hashMap.put( "DOCUMENTO", vendaDao.findTbVenda( codigo ).getFkDocumento().getDesignacao() );
//        hashMap.put( "SOFTWARE_VERSION", VERSION_SOFTWARE );
//        hashMap.put( "SOFTWARE_NAME", NAME_SOFTWARE );
//        hashMap.put( "REF_COD_FACT", getRefCodFact( vendaDao.findTbVenda( codigo ).getRefCodFact() ) );
//        hashMap.put( "STATUS_DOCUMENTO", this.status_documento );
//        hashMap.put( "MOTIVO_ISENCAO", this.motivo_isencao );
//        hashMap.put( "NIF_CLIENTE_CONSOMIDOR_FINAL", setConsumidorFinal( vendaDao.findTbVenda( codigo ) ) );
            hashMap.put( "PK_COMPRA", compras.getPkCompra() );
            String relatorio = CAMINHO_REPORT + "compra_recibo.jasper";;

            File file = new File( relatorio ).getAbsoluteFile();
            String obterCaminho = file.getAbsolutePath();

            try
            {
                JasperFillManager.fillReport( obterCaminho, hashMap, connection );
                JasperPrint jasperPrint = JasperFillManager.fillReport( obterCaminho, hashMap, connection );
                if ( jasperPrint.getPages().size() >= 1 )
                {
                    JasperViewer jasperViewer = new JasperViewer( jasperPrint, false );
                    jasperViewer.setVisible( true );
                }
                else
                {
                    JOptionPane.showMessageDialog( null, "Nao Existem Operações!..." );
                }
            }
            catch ( JRException jex )
            {
                jex.printStackTrace();
                //System.out.println("aqui");
                JOptionPane.showMessageDialog( null, "FALHA AO TENTAR MOSTRAR A COMPRA!..." );
            }
            catch ( Exception ex )
            {
                ex.printStackTrace();
                JOptionPane.showMessageDialog( null, "ERRO AO EFECTUAR A COMPRA!..." );
            }
        }
        catch ( SQLException ex )
        {
            Logger.getLogger( CompraVisao.class.getName() ).log( Level.SEVERE, null, ex );
        }
    }

