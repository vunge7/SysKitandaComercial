    private void busca_saidas_produtos(int codigo){
        
        TbSaidasProdutos saidasProdutos =   saidasProdutosDao.findTbSaidasProdutos(codigo);

    }
    
    private void procedimento_busca_saidas_produtos(){
                
                int   codigo_saida   = Integer.parseInt(   txtPesquisaNumero.getText() );
                
                try{
                    busca_saidas_produtos(codigo_saida);
                }
                catch(Exception ex)
                {
//                    procedimento_limpar();
                    JOptionPane.showMessageDialog(null, "Não existem saidas de produtos relacionados com este número", "AVISO", JOptionPane.WARNING_MESSAGE);
                }
        
    }
    
