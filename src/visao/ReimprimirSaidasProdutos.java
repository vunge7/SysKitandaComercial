        private void adicionar_saidas()
    {       
        try {
             List<TbSaidasProdutos>    saidasProdutoses = saidasProdutosDao.buscaTodos();
             lista_saidas.clear();

             for (int i = 0; i < saidasProdutoses.size(); i++) {
                    lista_saidas.addElement(   
                    saidasProdutoses.get(i).getPkSaidasProdutos()  );

             }
       
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }

