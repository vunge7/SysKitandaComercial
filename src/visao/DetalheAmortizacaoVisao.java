    private void preencher_tabela()
    {
        DefaultTableModel modelo = ( DefaultTableModel ) jTable1.getModel();
        modelo.setRowCount( 0 );
        for ( Amortizacao amortizacao : list_amortizacoes )
        {
            modelo.addRow( new Object[]
            {

                amortizacao.getPkAmortizacao(),
                MetodosUtil.getDataBanco( amortizacao.getData() ),
                MetodosUtil.getHoraBanco( amortizacao.getHora() ),
                amortizacao.getFkUsuario().getNome(),
                amortizacao.getValor()
            } );
        }

    }

