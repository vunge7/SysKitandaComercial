    private void abertura_primeiro_turno()
    {

        this.turno = new Turno();
        this.turno.setData( new Date() );
        this.turno.setHoraAbertura( new Date() );
        this.turno.setHoraFecho( null );
        this.turno.setTurno( DVML.PRIMEIRO_TURNO );
        this.turno.setFkUsuario( usuarioDao.findTbUsuario( this.id_user ) );

        try
        {
            turnoDao.create( turno );
            controlo_turno();
            JOptionPane.showMessageDialog( null, "Abertura efectuada com sucesso!..." );
        }
        catch ( Exception e )
        {
            JOptionPane.showMessageDialog( null, "Falha na abertura do 1º turno!...", "ERRO", JOptionPane.ERROR_MESSAGE );
        }

    }

    private void abertura_segundo_turno()
    {

        if ( turnoDao.existe_fecho_by_data( new Date(), DVML.PRIMEIRO_TURNO ) )
        {

            this.turno = new Turno();
            this.turno.setData( new Date() );
            this.turno.setHoraAbertura( new Date() );
            this.turno.setHoraFecho( null );
            this.turno.setTurno( DVML.SEGUNDO_TURNO );
            this.turno.setFkUsuario( usuarioDao.findTbUsuario( this.id_user ) );

            try
            {
                turnoDao.create( turno );
                controlo_turno();
                JOptionPane.showMessageDialog( null, "Abertura efectuada com sucesso!..." );
            }
            catch ( Exception e )
            {
                JOptionPane.showMessageDialog( null, "Falha na abertura do 2º turno!...", "ERRO", JOptionPane.ERROR_MESSAGE );
            }

        }
        else
        {
            JOptionPane.showMessageDialog( null, "Impossivel abrir o 2º turno sem fechar o 1º turno!...", "ERRO", JOptionPane.ERROR_MESSAGE );
        }

    }

    private void fecho_primeiro_turno()
    {

        if ( turnoDao.existe_abertura_by_data( new Date(), DVML.PRIMEIRO_TURNO ) )
        {

            if ( JOptionPane.YES_OPTION == JOptionPane.showConfirmDialog( null, "Caro usuário tens a certeza que pretende efectuar o fechar 1º turno?" ) )
            {

                this.turno = turnoDao.getTurnoByData( new Date(), DVML.PRIMEIRO_TURNO );
                this.turno.setHoraFecho( new Date() );
                try
                {
                    turnoDao.edit( turno );
                    controlo_turno();
                    JOptionPane.showMessageDialog( null, "Fecho efectuado com sucesso!..." );
                    new ListaVendasPrimeiroTurno( turnoDao.findTurno( turnoDao.last_turno_by_designacao_primeiro_segundo_turno( DVML.PRIMEIRO_TURNO ) ) );
                }
                catch ( Exception e )
                {
                    JOptionPane.showMessageDialog( null, "Falha no feicho do 1º Turno!...", "ERRO", JOptionPane.ERROR_MESSAGE );
                }

            }
            else
            {
                JOptionPane.showMessageDialog( null, "Operação cancelada", "INFO", JOptionPane.INFORMATION_MESSAGE );
            }

        }
        else
        {
            JOptionPane.showMessageDialog( null, "Impossivel dar o fecho sem dar abertura do 1º Turno!...", "ERRO", JOptionPane.ERROR_MESSAGE );
        }

    }

