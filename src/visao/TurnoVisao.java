    private void mostrar_data()
    {

        Date data = new Date();
        String dia = String.valueOf( data.getDate() );
        String mes = String.valueOf( data.getMonth() + 1 );
        String ano = String.valueOf( data.getYear() + 1900 );;

        lb_data.setText( "Data: " + dia + "/" + mes + "/" + ano );
    }

    private void controlo_turno()
    {

        /*1 - TURNO*/
        if ( turnoDao.existe_abertura_by_data( new Date(), DVML.PRIMEIRO_TURNO ) )
        {
            btn_abertura_primeiro_turno.setEnabled( false );
        }
        else
        {
            btn_abertura_primeiro_turno.setEnabled( true );
        }

        if ( turnoDao.existe_fecho_by_data( new Date(), DVML.PRIMEIRO_TURNO ) )
        {
            btn_fecho_primeiro_turno.setEnabled( false );
        }
        else
        {
            btn_fecho_primeiro_turno.setEnabled( true );
        }

        /*2 - TURNO*/
        if ( turnoDao.existe_abertura_by_data( new Date(), DVML.SEGUNDO_TURNO ) )
        {
            btn_abertura_segundo_turno.setEnabled( false );
        }
        else
        {
            btn_abertura_segundo_turno.setEnabled( true );
        }

        if ( turnoDao.existe_fecho_by_data( new Date(), DVML.SEGUNDO_TURNO ) )
        {
            btn_fecho_segundo_turno.setEnabled( false );
        }
        else
        {
            btn_fecho_segundo_turno.setEnabled( true );
        }

    }

