    private void actualizar()
    {
       cmbBanco.setModel(   new DefaultComboBoxModel( (Vector) bancoDao.buscaTodos() ) );
    }
    public void salvar()
    {
        if( !bancoDao.exist_banco(  txtDescricao.getText() ) )
        {
                try {
                            banco = new TbBanco();
                            setDadosBanco();
                            bancoDao.create(banco);
                            actualizar();
                            limpar();
                            JOptionPane.showMessageDialog(null, "Dados salvo com sucesso!...");   
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Erro ao cadastrar o tipo de pagamento ", "ERRO", JOptionPane.ERROR_MESSAGE);
                }
        
        }else  JOptionPane.showMessageDialog(null, "Este tipo de pagamento ja existe ", "AVISO", JOptionPane.WARNING_MESSAGE);
              
    }
    
    
    public void setDadosBanco(){
                    
                //banco = new TbBanco();
                banco.setNumero( txtContaNumero.getText()    );
                banco.setDescrisao(   txtDescricao.getText() );
                banco.setSaldoBanco(0.0);
    }
    
    
    private void setDados()
    {
            banco = bancoDao.findTbBanco( getIdBanco() );
            
            txtDescricao.setText(   banco.getDescrisao() );
            txtContaNumero.setText(   banco.getNumero()   );
            txtSaldo.setText(String.valueOf(banco.getSaldoBanco()));
       
    }
    
    
    
    
    
    public void alterar()
    {
       
        if( bancoDao.exist_banco(  String.valueOf(  cmbBanco.getSelectedItem() ) ) )
        {
                try {
                    
                            banco = bancoDao.findTbBanco( getIdBanco() );
                            setDadosBanco();
                            bancoDao.edit(banco);
                            actualizar();
                            limpar();
                            JOptionPane.showMessageDialog(null, "Dados alterados com sucesso!...");  
                            
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Erro ao alterar o tipo de pagamento ", "ERRO", JOptionPane.ERROR_MESSAGE);
                }
        
        }else  JOptionPane.showMessageDialog(null, "Este tipo de pagamento não ja existe ", "AVISO", JOptionPane.WARNING_MESSAGE);
    
    }
    
     public void limpar() {
            txtDescricao.setText("");
            txtContaNumero.setText("");
     }
        
    
