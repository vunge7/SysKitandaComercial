    private void fazerRestore()
    {
        // üîπ Define a pasta onde ficam os backups
        File pastaBackup = new File( "C:\\Kitanda\\Backups" ); // altere para a sua pasta
        if ( !pastaBackup.exists() || !pastaBackup.isDirectory() )
        {
            JOptionPane.showMessageDialog( this, "A pasta de backups n√£o existe: " + pastaBackup.getAbsolutePath() );
            return;
        }

        // üîπ Lista os arquivos SQL e pega o mais recente
        File[] arquivos = pastaBackup.listFiles( ( dir, name ) -> name.toLowerCase().endsWith( ".sql" ) );
        if ( arquivos == null || arquivos.length == 0 )
        {
            JOptionPane.showMessageDialog( this, "Ainda n√£o existe backup para restaurar!" );
            return;
        }

        Arrays.sort( arquivos, Comparator.comparingLong( File::lastModified ).reversed() );
        File ultimoBackup = arquivos[ 0 ]; // backup mais recente
        System.out.println( "üîπ √öltimo backup encontrado: " + ultimoBackup.getAbsolutePath() );

        // üîπ Restaura o backup
        try ( Connection conn = DriverManager.getConnection( url, usuario, senha ); BufferedReader reader = new BufferedReader( new FileReader( ultimoBackup ) ) )
        {

            Statement st = conn.createStatement();
            int count = 0;

            // üîπ Desativa temporariamente as foreign keys
            st.execute( "SET FOREIGN_KEY_CHECKS=0" );

            // üîπ Descobre tabelas e trunca
            Set<String> tabelas = new HashSet<>();
            try ( BufferedReader reader2 = new BufferedReader( new FileReader( ultimoBackup ) ) )
            {
                String linha;
                while ( ( linha = reader2.readLine() ) != null )
                {
                    linha = linha.trim();
                    if ( linha.startsWith( "INSERT INTO" ) )
                    {
                        String nomeTabela = linha.split( "`" )[ 1 ];
                        if ( !tabelas.contains( nomeTabela ) )
                        {
                            try
                            {
                                st.execute( "TRUNCATE TABLE `" + nomeTabela + "`" );
                                tabelas.add( nomeTabela );
                                System.out.println( "Tabela " + nomeTabela + " truncada antes do restore." );
                            }
                            catch ( Exception e )
                            {
                                System.err.println( "‚ö†Ô∏è Falha ao truncar tabela " + nomeTabela + ": " + e.getMessage() );
                            }
                        }
                    }
                }
            }

            // üîπ Executa os INSERTs
            String linha;
            while ( ( linha = reader.readLine() ) != null )
            {
                linha = linha.trim();
                if ( !linha.isEmpty() && linha.startsWith( "INSERT INTO" ) )
                {
                    try
                    {
                        st.executeUpdate( linha );
                        count++;
                    }
                    catch ( Exception e )
                    {
                        System.err.println( "‚ö†Ô∏è Falha ao executar INSERT: " + e.getMessage() );
                    }
                }
            }

            // üîπ Reativa as foreign keys
            st.execute( "SET FOREIGN_KEY_CHECKS=1" );

            JOptionPane.showMessageDialog( this, "‚úÖ Restore conclu√≠do! " + count + " registros restaurados." );

        }
        catch ( Exception ex )
        {
            ex.printStackTrace();
            JOptionPane.showMessageDialog( this, "‚ùå Erro no restore: " + ex.getMessage() );
        }
    }


