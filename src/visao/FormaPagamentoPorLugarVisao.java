    private boolean referencia_valida()
    {

        DefaultTableModel modelo = ( DefaultTableModel ) tabela_forma_pagamento.getModel();

        for ( int i = 0; i < modelo.getRowCount(); i++ )
        {
            String forma = modelo.getValueAt( i, 1 ).toString();
            String referencia = modelo.getValueAt( i, 2 ).toString();
            double valor = Double.parseDouble( modelo.getValueAt( i, 3 ).toString() );
            int id_forma_pagamento = formaPagamentoDao.getIdByDescricao( forma );
            if ( formaPagamentoDao.getIdByDescricao( forma ) != DVML.NUMERARIO && valor > 0 )
            {
                if ( referencia.equals( "" ) )
                {
                    JOptionPane.showMessageDialog( null, "Caro usuário insira a referência da linha nº " + ( i + 1 ) );
                    return false;
                }
                else if ( FormaPagamentoItemDao.existe_referencia( referencia, id_forma_pagamento, conexao ) )
                {
                    JOptionPane.showMessageDialog( null, "já existe a referencia (" + referencia + ") no local " + forma.toUpperCase() + "\nna base de dados." );
                    return false;
                }
            }

        }
        return true;

    }
