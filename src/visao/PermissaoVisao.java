    private void adicionar_armazem_list_model()
    {
        List<TbArmazem> armazens = armazemDao.getArmazens1();
        list_model.clear();
        for ( int i = 0; i < armazens.size(); i++ )
        {
            list_model.addElement( armazens.get( i ).getDesignacao() );
        }
    }

    private void adicionar_accesso_armazem()
    {
        int idUsuario = getIdUsuario();
        List<AccessoArmazem> allAccessoByIdUSuario = accessoArmazemDao.getAllAccessoByIdUSuario( idUsuario );

        DefaultTableModel modelo = ( DefaultTableModel ) tabela_accesso.getModel();
        modelo.setRowCount( 0 );

        if ( !Objects.isNull( allAccessoByIdUSuario ) )
        {

            for ( AccessoArmazem accessoArmazem : allAccessoByIdUSuario )
            {
                modelo.addRow( new Object[]
                {
                    accessoArmazem.getPkAccessoArmazem(),
                    accessoArmazem.getFkArmazem().getDesignacao(),
                    accessoArmazem.getFkUsuario().getNome()
                } );
            }
        }

    }

    private TbArmazem getArmazem( String designacao )
    {
        return armazemDao.getArmazemByDescricao( designacao );
    }

    private void adicionar_accesso()
    {
        Object[] selectedValues = list_armazem.getSelectedValues();
        int idUsuario = getIdUsuario();
        boolean sucesso = true;
        if ( !Objects.isNull( selectedValues ) )
        {
            for ( Object selectedValue : selectedValues )
            {
                try
                {

                    boolean existe_accesso = accessoArmazemDao.existe_accesso( idUsuario, getArmazem( String.valueOf( selectedValue ) ).getCodigo() );

                    if ( !existe_accesso )
                    {
                        AccessoArmazem accessoArmazem = new AccessoArmazem();
                        accessoArmazem.setDataTime( new Date() );
                        accessoArmazem.setFkArmazem( getArmazem( String.valueOf( selectedValue ) ) );
                        accessoArmazem.setFkUsuario( usuarioDao.findTbUsuario( idUsuario ) );
                        accessoArmazemDao.create( accessoArmazem );
                    }

                }
                catch ( Exception e )
                {
                    sucesso = false;
                    e.printStackTrace();
                    System.err.println( "Falha ao registrar o accesso: " + e.getMessage() );
                }
            }
            if ( sucesso )
            {
                adicionar_accesso_armazem();
                JOptionPane.showMessageDialog( null, "Accesso atribuido com sucesso." );
            }

        }

    }

    private void setArmazem( String armazem )
    {
        if ( !armazem.equalsIgnoreCase( "Multi_armazem" ) )
        {
            jTabbedPane1.setEnabled( true );

        }
        else
        {
            jTabbedPane1.setEnabled( true );

        }
    }

    private void remover_accesso()
    {

        int opcao = JOptionPane.showConfirmDialog( null, "Deseja remanete remover o accesso neste armazem para este usuario?" );
        boolean sucesso = true;
        if ( opcao == JOptionPane.YES_OPTION )
        {

            int[] selectedRow = tabela_accesso.getSelectedRows();

            if ( selectedRow.length >= 0 )
            {

                for ( int i : selectedRow )
                {
                    DefaultTableModel modelo = ( DefaultTableModel ) tabela_accesso.getModel();
                    int id_linha = Integer.parseInt( modelo.getValueAt( i, 0 ).toString() );

                    try
                    {
                        accessoArmazemDao.destroy( id_linha );
                    }
                    catch ( Exception e )
                    {
                        e.printStackTrace();
                        accessoArmazemDao.getEntityManager().getTransaction().rollback();
                        sucesso = false;
                    }
                }

                if ( sucesso )
                {
                    adicionar_accesso_armazem();
                    JOptionPane.showMessageDialog( null, "Accesso removido com sucesso." );
                }

            }
            else
            {
                JOptionPane.showMessageDialog( null, "Seleccione o armazem para retirar o accesso." );
            }

        }

    }

