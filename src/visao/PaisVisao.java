    public void alterar()
    {
       
       if(! vazios_campos())
       {
                try {
                            pais = paisDao.findTbPais(  this.cod_pais );
                            setDadosPais();
                            paisDao.edit(pais);
                            actualizar();
                            limpar();
                            JOptionPane.showMessageDialog(null, "Dados alterados com sucesso!...");  
                            
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Erro ao alterar a pais ", "ERRO", JOptionPane.ERROR_MESSAGE);
                }
       }else JOptionPane.showMessageDialog(null, "Pf. seleccione a pais para alterar");
        
    
    }
    
     public void limpar() {
            txtDescricao.setText("");
     }
        
    
