    private void adicionar_tabela( List<TbPais> pais)
    {
     
            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();

            modelo.setRowCount(0);
            for (int i = 0; i < pais.size(); i++) 
            {
               modelo.addRow(new Object[]{
               pais.get(i).getIdPais(),
               pais.get(i).getDescrisao()
               
            });
            }

    }

    
    public void setDadosPaisModelo()
    {
    
            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
     
            txtDescricao.setText(  String.valueOf( modelo.getValueAt(jTable1.getSelectedRow(), 1) ) );
       
            this.cod_pais =   Integer.parseInt( String.valueOf( modelo.getValueAt(jTable1.getSelectedRow(), 0) ) );

    }
    
    
    
    
