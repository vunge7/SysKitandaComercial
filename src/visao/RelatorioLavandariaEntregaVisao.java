    private void mostrarTodosClientes()
    {
        List<TbCliente> listaClientes = vendasController.listarAllClientesLavandariaEntregas();
        DefaultTableModel modelo = ( DefaultTableModel ) jTable1.getModel();
        modelo.setRowCount( 0 );
        if ( Objects.nonNull( listaClientes ) )
        {
            jTable1.setRowHeight( 30 );
            for ( TbCliente cliente : listaClientes )
            {
                modelo.addRow( new Object[]
                {
                    cliente.getCodigo(),
                    cliente.getNome(),
                    cliente.getTelefone()
                } );
            }
        }
    }

    private int getIdClienteSelecionado()
    {
        DefaultTableModel modelo = ( DefaultTableModel ) jTable1.getModel();
        int linhaSelecionada = jTable1.getSelectedRow();
        return Integer.parseInt( modelo.getValueAt( linhaSelecionada, 0 ).toString() );
    }

    private String getEntregaID()
    {
        try
        {
            DefaultTableModel modelo = ( DefaultTableModel ) jTableRecolha.getModel();
            int linhaSelecionada = jTableRecolha.getSelectedRow();
            return modelo.getValueAt( linhaSelecionada, 0 ).toString();
        }
        catch ( Exception e )
        {
            return "";
        }

    }

    private void adicionarRelatorioPorCliente()
    {
        int idCliente = getIdClienteSelecionado();
        List<VendasController.RelatorioRecolhaLavandaria> relatorio = vendasController.relatorioRecolhaLavandariaByIdCliente( idCliente );

        jTableRecolha.setRowHeight( 30 );
        DefaultTableModel modelo = ( DefaultTableModel ) jTableRecolha.getModel();
        modelo.setRowCount( 0 );

        if ( Objects.nonNull( relatorio ) )
        {
            for ( VendasController.RelatorioRecolhaLavandaria item : relatorio )
            {
                String codFact = item.getCodFact();
                String dataRecolha = MetodosUtil.getDataBanco( item.getData() );
                int numeroPecasEntregues = item.getPecasEntregue();
                int numeroPecasPorEntregar = item.getPecasPorEntregar();

                modelo.addRow( new Object[]
                {
                    codFact,
                    dataRecolha,
                    numeroPecasEntregues,
                    numeroPecasPorEntregar
                } );
            }
        }

    }

    private void adicionarDetalheDoc()
    {
        String entregaID = getEntregaID();
        List<VendasController.PecasEntregues> relatorio = vendasController.relatorioPecasEntreguesByCodFact( entregaID );
        jTablePecasEntregues.setRowHeight( 30 );
        DefaultTableModel modelo = ( DefaultTableModel ) jTablePecasEntregues.getModel();
        modelo.setRowCount( 0 );

        if ( Objects.nonNull( relatorio ) )
        {
            for ( VendasController.PecasEntregues item : relatorio )
            {

                String codFact = item.getDoc();
                int codServico = item.getCodServico();
                String designacao = item.getDesignacao();
                String usuario = item.getUsuario();

                modelo.addRow( new Object[]
                {
                    codServico,
                    designacao,
                    codFact,
                    usuario
                } );
            }
        }
    }

    private void adicionarDetalheDocPorEntregar()
    {
        String entregaID = getEntregaID();
        List<VendasController.PecasPorEntregar> relatorio = vendasController.relatorioPorEntregarByCodFact( entregaID );
        jTablePorEntregar.setRowHeight( 30 );
        DefaultTableModel modelo = ( DefaultTableModel ) jTablePorEntregar.getModel();
        modelo.setRowCount( 0 );

        if ( Objects.nonNull( relatorio ) )
        {
            for ( VendasController.PecasPorEntregar item : relatorio )
            {

                int codServico = item.getCodServico();
                String obs = item.getObs();
                String designacao = item.getDesignacao();

                modelo.addRow( new Object[]
                {
                    codServico,
                    designacao,
                    obs
                } );
            }
        }
    }

