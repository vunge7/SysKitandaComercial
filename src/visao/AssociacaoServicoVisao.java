    private void setDadosCabecario()
    {
        if ( codPai > 0 )
        {
            TbProduto produto = produtosController.findByCod( codPai );
            lbProduto.setText( produto.getDesignacao() );
        }
        else
        {
            lbProduto.setText( "" );
        }
    }

    private void adicionarDadosTabela()
    {
        List<TbProduto> listarTodosByCodPai = produtosController.listarTodosByCodPai( codPai );

        DefaultTableModel modelo = ( DefaultTableModel ) jTable1.getModel();
        modelo.setRowCount( 0 );
        jTable1.setRowHeight( 50 );
        if ( Objects.nonNull( listarTodosByCodPai ) )
        {

            for ( int i = 0; i < listarTodosByCodPai.size(); i++ )
            {
                TbProduto get = listarTodosByCodPai.get( i );

                TbPreco preco = precosController.getLastIdPrecoByIdProduto( get.getCodigo(), 1 );

                modelo.addRow( new Object[]
                {
                    get.getCodigo(),
                    get.getDesignacao(),
                    preco.getPrecoVenda()

                } );

            }
        }

    }

    public static void adcionarItem( int codigo )
    {

        if ( !existeProdutoAgregado( codigo ) )
        {

            if ( codigo == codPai )
            {
                JOptionPane.showMessageDialog( null, "Impossível agregar", "Aviso", JOptionPane.WARNING_MESSAGE );
                return;
            }

            TbProduto produto = produtosController.findByCod( codigo );
            DefaultTableModel modelo = ( DefaultTableModel ) jTable1.getModel();
            TbPreco preco = precosController.getLastIdPrecoByIdProduto( produto.getCodigo(), 1 );

            modelo.addRow( new Object[]
            {
                produto.getCodigo(),
                produto.getDesignacao(),
                preco.getPrecoVenda()
            } );

            if ( !produtosController.actualizarCodPai( codigo, codPai ) )
            {
                JOptionPane.showMessageDialog( null, "Falha na agregação do serviço", "Falha", JOptionPane.ERROR_MESSAGE );
            }
        }
        else
        {
            JOptionPane.showMessageDialog( null, "Este serviço já está agregado.", "Falha", JOptionPane.ERROR_MESSAGE );
        }

    }

    private void procedimentoActualizar()
    {
        DefaultTableModel modelo = ( DefaultTableModel ) jTable1.getModel();
        boolean sucesso = true;
        if ( jTable1.getRowCount() > 0 )
        {
            for ( int i = 0; i < jTable1.getRowCount(); i++ )
            {
                int cod = Integer.parseInt( modelo.getValueAt( i, 0 ).toString() );
                if ( !produtosController.actualizarCodPai( cod, codPai ) )
                {
                    sucesso = false;
                    JOptionPane.showMessageDialog( null, "Falha na agregação do serviço", "Falha", JOptionPane.ERROR_MESSAGE );

                    break;
                }
            }

            if ( sucesso )
            {
                JOptionPane.showMessageDialog( null, "Agregação efectuado com sucesso!..." );
            }
        }
        else
        {
            JOptionPane.showMessageDialog( null, "Adiciona o produto na tabela." );
        }

    }

    private static boolean existeProdutoAgregado( int codigoProduto )
    {
        DefaultTableModel modelo = ( DefaultTableModel ) jTable1.getModel();
        for ( int i = 0; i < jTable1.getRowCount(); i++ )
        {
            int cod = Integer.parseInt( modelo.getValueAt( i, 0 ).toString() );
            if ( cod == codigoProduto )
            {
                return true;
            }
        }
        return false;

    }

