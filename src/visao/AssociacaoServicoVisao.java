    private void procedimentoRemoverProduto()
    {

        if ( jTable1.getSelectedRow() < 0 )
        {
            JOptionPane.showMessageDialog( null, "Selecione a linha." );
            return;
        }

        int opcao = JOptionPane.showConfirmDialog( null, "Tem a certeza que pretendes remover este serviço ?" );
        if ( opcao == JOptionPane.YES_OPTION )
        {
            DefaultTableModel modelo = ( DefaultTableModel ) jTable1.getModel();

            int linhaSeleccionada = jTable1.getSelectedRow();
            int cod = Integer.parseInt( jTable1.getValueAt( linhaSeleccionada, 0 ).toString() );

            modelo.removeRow( linhaSeleccionada );

            if ( produtosController.eliminarCodPai( cod ) )
            {
                adicionarDadosTabela();
            }
            else
            {
                JOptionPane.showMessageDialog( null, "Falha na desagregação do serviço", "Falha", JOptionPane.ERROR_MESSAGE );
            }

        }

    }

