    private boolean campo_validos()
    {
        if ( txtJustificacao.getText().equals( "" ) )
        {

            showMessageUtil( "Caro usuário insira a justificação.", TIPO_MENSAGEM_INFOR );
            txtJustificacao.requestFocus();
            return false;
        }

        return true;
    }

    private void enviar_justificativa()
    {
        if ( campo_validos() )
        {
            /**
             * Pseudo código
             *
             * 1. pegar o o objecto (falta) por intermédio do código. 
             * 2. actualizar o campo justificada_injustificadfa para 'true'.
             * 3.preencher o campo descriccao_justifiva. 
             * 4. preencher o campo data_justificativa.
             */

            TbFalta falta = faltaDao.findTbFalta( this.codFalta );
            falta.setJustificadaInjustificada( true );
            falta.setDescricaoFalta( txtJustificacao.getText() );
            falta.setDataJustificativa( new Date() );

            try
            {
                faltaDao.edit( falta );
                //actualizar as tabelas
                dispose();
                ExtraSalarioFuncionarioVisao.desenhar_tabelas_justificadas_e_injustificadas();
            }
            catch ( Exception e )
            {
            }

        }

    }

