    private void adicionar_tabela( List<Modalidade> modalidade) {
     
            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
            modelo.setRowCount(0);
            for (int i = 0; i < modalidade.size(); i++) 
            {
               modelo.addRow(new Object[]{
               modalidade.get(i).getPkModalidade(),
               modalidade.get(i).getDesignacao(),
               modalidade.get(i).getDiasUteisTrabalho()
               
            });
            }

    }

    public void setDadosModalidadeModelo() {
    
            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
            txtDescricaoTurno.setText(  String.valueOf( modelo.getValueAt(jTable1.getSelectedRow(), 1) ) );       
            txtDias.setText(  String.valueOf( modelo.getValueAt(jTable1.getSelectedRow(), 2) ) );       
            this.cod_modalidade =   Integer.parseInt( String.valueOf( modelo.getValueAt(jTable1.getSelectedRow(), 0) ) );

    }
    
    private void actualizar(){
        adicionar_tabela(  modalidadeDao.buscaTodasModalidades());
    }
    
    public void salvar(){
        
        if(!vazios_campos() )
        {
                if( !modalidadeDao.exist_modalidade(txtDescricaoTurno.getText() ) )
                {
                        try {
                                    modalidade = new Modalidade();
                                    preparar_objecto();
                                    modalidadeDao.create(modalidade);
                                    actualizar();
                                    limpar();
                                    JOptionPane.showMessageDialog(null, "Dados salvo com sucesso!...");   
                        } catch (Exception e) {
                            JOptionPane.showMessageDialog(null, "Erro ao cadastrar a modalidade ", "ERRO", JOptionPane.ERROR_MESSAGE);
                        }

                }else  JOptionPane.showMessageDialog(null, "Esta modalidade ja existe", "AVISO", JOptionPane.WARNING_MESSAGE);
        }else JOptionPane.showMessageDialog(null, "Pf digita a modalidade ", "AVISO", JOptionPane.WARNING_MESSAGE);
    }
    
    private boolean vazios_campos(){
            return txtDescricaoTurno.getText().equals("");   
    }
    
    public void preparar_objecto(){
                modalidade.setDesignacao(txtDescricaoTurno.getText() );
                modalidade.setDiasUteisTrabalho(Integer.parseInt( txtDias.getText() ));
    }
    
    public void alterar(){
       
       if(! vazios_campos())
       {
                try {
                            modalidade = modalidadeDao.findModalidade(this.cod_modalidade );
                            preparar_objecto();
                            modalidadeDao.edit(modalidade);
                            actualizar();
                            limpar();
                            JOptionPane.showMessageDialog(null, "Dados alterados com sucesso!...");  
                            
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Erro ao alterar a modalidade ", "ERRO", JOptionPane.ERROR_MESSAGE);
                }
       }else JOptionPane.showMessageDialog(null, "Pf. seleccione a modalidade para alterar");
    
    }
    
    public void limpar() {
            txtDescricaoTurno.setText("");
            txtDias.setText("");
    }
    
