  public void salvar()
  {
    
      
        Vector<String> vector = getItemSelecionado();
        TbItemPermissao itemPermissao;
        int codigo_usuario = getIdUsuario();
        long codigo_permissao = 0;
        boolean salvou = true;

        itemPermissaoDao.deletar_items(codigo_usuario, DVML.MODULO_RH_TESOURARIA);

        for (int i = 0; i < vector.size(); i++) {
            
     
             codigo_permissao = permissaoDao.getIdByDescricao( vector.get(i)   );
              System.err.println("cod: "  +codigo_permissao);
           
            try {
                
                  itemPermissao = new TbItemPermissao();
                  itemPermissao.setIdPermissao(   permissaoDao.findTbPermissao(  codigo_permissao    )   );
                  itemPermissao.setIdUsuario(usuarioDao.findTbUsuario(getIdUsuario() ) );
                  itemPermissao.setStatus("true");
                  itemPermissao.setModulo(DVML.MODULO_RH_TESOURARIA);
                  itemPermissaoDao.create(itemPermissao);
               
            } catch (Exception e) {
                e.printStackTrace();
               
                salvou = false;
                JOptionPane.showMessageDialog(null, "Falha ao dar algumas permissões ao utilizador!...", "ERROR", JOptionPane.ERROR_MESSAGE);
                break;
            }

        }

        if (salvou) {
            JOptionPane.showMessageDialog(null, "Operação efectuada com sucesso!...");
        }

    
    }
  
