                      jcbProcessamentoSalarios.setSelected(true);

                /*RH*/
                if(permissao.equals("Funcionario"))
                      jcbFuncionario.setSelected(true);
                else if(permissao.equals("Subsidios"))
                      jcbSubsidios.setSelected(true);
                else if(permissao.equals("Marcacao de Faltas"))
                      jcbMarcacaoFaltas.setSelected(true);
                if(permissao.equals("Adiantamento de Salario"))
                      jcbAdiantamentoSalario.setSelected(true);
