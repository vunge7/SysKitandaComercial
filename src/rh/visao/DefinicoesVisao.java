    private void visulizar_botao_anular( int idAnoEconomico, int idPeriodo, JButton btnClose )
    {

        btnClose.setVisible( false );
        if ( fechoPeriodoDao.existe_periodo( idAnoEconomico, idPeriodo ) )
        {
            FechoPeriodo periodoActual = fechoPeriodoDao.getFechoPeriodoByIdAnoAndPerido( idAnoEconomico, idPeriodo );
            FechoPeriodo periodoPosterior = fechoPeriodoDao.getFechoPeriodoByIdAnoAndPerido( idAnoEconomico, idPeriodo + 1 );
            if ( fechoPeriodoDao.existe_abertura( periodoActual ) && !fechoPeriodoDao.existe_abertura( periodoPosterior ) )
            {
                btnClose.setVisible( true );
                resizeJButtonIcon( largura, altura, getClass().getResource( path_eliminar ), btnClose );
            }
            else
            {
                btnClose.setVisible( false );
            }
        }
    }

