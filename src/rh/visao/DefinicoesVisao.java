    public static void procedimentoAbrirPeriodo( int idAnoEconomico, int idPerido, Date dataAbertura )
    {
        fechoPeriodo = new FechoPeriodo();
        fechoPeriodo.setDataAbertura( dataAbertura );
        fechoPeriodo.setDataFecho( null );
        fechoPeriodo.setFkAno( anoDao.findTbAno( idAnoEconomico ) );
        fechoPeriodo.setFkPeriodo( mesRhDao.findTbMesRh( idPerido ) );
        fechoPeriodo.setFkUsuario( usuarioDao.findTbUsuario( idUsuario ) );

        try
        {
            fechoPeriodoDao.create( fechoPeriodo );
            MetodosUtil.showMessageUtil( "Período aberto com sucesso.", DVML.TIPO_MENSAGEM_INFOR );

        }
        catch ( Exception e )
        {
            e.printStackTrace();
            MetodosUtil.showMessageUtil( "Falha ao abrir o período", DVML.TIPO_MENSAGEM_ERRO );

        }

    }

