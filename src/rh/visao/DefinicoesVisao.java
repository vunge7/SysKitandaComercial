    public static void procedimentoFechar( int idPeriodo )
    {

        int opcao = JOptionPane.showConfirmDialog( null, "Caro usuário tens a plena certeza fechar este período?\nObs:Uma vez fechado não voltarás a abrir." );

        if ( opcao == JOptionPane.YES_OPTION )
        {

            FechoPeriodo periodoActual = fechoPeriodoDao.getFechoPeriodoByIdAnoAndPerido( getIdAnoEconomico(), idPeriodo );
            fechoPeriodo = fechoPeriodoDao.getFechoPeriodoByIdAnoAndPerido( getIdAnoEconomico(), idPeriodo );

            fechoPeriodo.setDataFecho( data_fecho_periodo );
            try
            {
                if ( !Objects.isNull( periodoActual ) )
                {
                    if ( MetodosUtil.maior_data_1_data_2( new Date(), periodoActual.getDataAbertura() ) || MetodosUtil.igual_data_1_data_2( new Date(), periodoActual.getDataAbertura() ) )
                    {
                        fechoPeriodoDao.edit( fechoPeriodo );
                    }
                    else
                    {
                        JOptionPane.showMessageDialog( null, "Caro usuário verifique a data do sistema" );
                    }
                }
                else
                {
                    fechoPeriodoDao.edit( fechoPeriodo );
                    data_fecho_periodo = null;
                }

            }
            catch ( Exception e )
            {
                e.printStackTrace();
                MetodosUtil.showMessageUtil( "Falha ao realizar o fecho do período", DVML.TIPO_MENSAGEM_INFOR );
            }

        }

    }

    private void visualizar_botao_abrir( int idAno, int idPeriodo, JButton button )
    {
        button.setVisible( false );
        if ( fechoPeriodoDao.existe_periodo( idAno, idPeriodo ) )
        {
            FechoPeriodo periodoAnterior = fechoPeriodoDao.getFechoPeriodoByIdAnoAndPerido( idAno, idPeriodo - 1 );
            FechoPeriodo periodoActual = fechoPeriodoDao.getFechoPeriodoByIdAnoAndPerido( idAno, idPeriodo );
            if ( fechoPeriodoDao.existe_fecho( periodoAnterior ) )
            {
                if ( !fechoPeriodoDao.existe_abertura( periodoActual ) )
                {
                    //visualiza
                    button.setVisible( true );
                    resizeJButtonIcon( largura, altura, getClass().getResource( path_open ), button );
                }

            }
            else
            {
                //não visualiza
                button.setVisible( false );
            }
        }
        else
        {

            //visualiza
            button.setVisible( true );
            resizeJButtonIcon( largura, altura, getClass().getResource( path_open ), button );
        }
    }

    private void visualizar_botao_fechar( int idAno, int idPeriodo, JButton button )
    {
        FechoPeriodo periodo = fechoPeriodoDao.getFechoPeriodoByIdAnoAndPerido( idAno, idPeriodo );
        button.setVisible( false );
        if ( fechoPeriodoDao.existe_periodo( idAno, idPeriodo ) )
        {
            if ( fechoPeriodoDao.existe_abertura( periodo ) )
            {
                //visualiza senão existe um fecho
                button.setVisible( !fechoPeriodoDao.existe_fecho( periodo ) );
                resizeJButtonIcon( largura, altura, getClass().getResource( path_close ), button );
            }
            else
            {
                //não visualiza
                button.setVisible( false );
            }
        }

    }

