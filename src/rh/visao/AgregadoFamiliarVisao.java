    private void init()
    {
        dcDataNascimento.setMaxSelectableDate( new Date() );
        double salario_base = FichaFuncionarioVisao.jsSalarioBase.getValue() != null ? ( Double ) FichaFuncionarioVisao.jsSalarioBase.getValue() : 0d;
        jsValorBono.setModel( CfMethodsSwing.criarSpinnerDoubleModel( 0.0, salario_base, 0.00 ) );
    }

    private boolean campo_validos()
    {
        if ( txtNomeFilho.getText().equals( "" ) )
        {
            txtNomeFilho.requestFocus();
            showMessageUtil( "Caro usuÃ¡rio digite o nome do filho do funcionÃ¡rio.", DVML.TIPO_MENSAGEM_ALERTA );
            return false;
        }
        else if ( dcDataNascimento.getCalendar() == null )
        {
            showMessageUtil( "Caro usuÃ¡rio seleccione da data de nascimento do filho.", DVML.TIPO_MENSAGEM_ALERTA );
        }
        else if ( ( Double ) jsValorBono.getValue() == 0d || jsValorBono.getValue() == null )
        {
            showMessageUtil( "Caro usuÃ¡rio digite o bono do filho. ", DVML.TIPO_MENSAGEM_ALERTA );
        }

        return true;
    }

    private void enviar_ficha_funcionario()
    {
        if ( campo_validos() )
        {
            String nome_filho = iniciais_maiuscula( txtNomeFilho.getText() );
            Date data_nascimento = dcDataNascimento.getDate();
            double valor_bono = ( Double ) jsValorBono.getValue();
            
            FichaFuncionarioVisao.adicionar_filho_agregado( nome_filho, data_nascimento, valor_bono );
            dispose();
            
        }

    }

