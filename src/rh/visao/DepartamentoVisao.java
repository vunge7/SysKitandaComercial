    private void adicionar_tabela( List<TbDepartamento> departamento )
    {

        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount( 0 );
        for ( int i = 0; i < departamento.size(); i++ )
        {
            modelo.addRow( new Object[]
            {
                departamento.get( i ).getPkDepartamento(),
                departamento.get( i ).getDesignacao()

            } );
        }

    }

    public void setDadosDepartamentoModelo()
    {

        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        txtDescricao.setText( String.valueOf( modelo.getValueAt( jTable1.getSelectedRow(), 1 ) ) );
        this.cod_departamento = Integer.parseInt( String.valueOf( modelo.getValueAt( jTable1.getSelectedRow(), 0 ) ) );

    }

    private void actualizar()
    {
        adicionar_tabela( departamentoDao.buscaTodosDepartamentoes() );
    }

    public void salvar()
    {

        if ( !vazios_campos() )
        {
            if ( !departamentoDao.exist_departamento( txtDescricao.getText() ) )
            {
                try
                {
                    departamento = new TbDepartamento();
                    preparar_objecto();
                    departamentoDao.create( departamento );
                    actualizar();
                    limpar();
                    JOptionPane.showMessageDialog( null, "Dados salvo com sucesso!..." );
                    if ( !Objects.isNull( FichaFuncionarioVisao.cmbDepartamento ) )
                    {
                        FichaFuncionarioVisao.cmbDepartamento.setModel( new DefaultComboBoxModel( departamentoDao.buscaTodosExtra() ) );
                        FichaFuncionarioVisao.cmbDepartamento.setSelectedItem( departamento.getDesignacao() );
                        dispose();
//                        ProdutosVisao.getDescricao();
                    }
                }
                catch ( Exception e )
                {
                    JOptionPane.showMessageDialog( null, "Erro ao cadastrar o departamento ", "ERRO", JOptionPane.ERROR_MESSAGE );
                }

            }
            else
            {
                JOptionPane.showMessageDialog( null, "Este departamento ja existe ", "AVISO", JOptionPane.WARNING_MESSAGE );
            }
        }
        else
        {
            JOptionPane.showMessageDialog( null, "Pf digita o departamento ", "AVISO", JOptionPane.WARNING_MESSAGE );
        }
    }

    private boolean vazios_campos()
    {
        return txtDescricao.getText().equals( "" );
    }

    public void preparar_objecto()
    {
        departamento.setDesignacao( txtDescricao.getText().toUpperCase() );
    }

    public void alterar()
    {

        if ( !vazios_campos() )
        {
            try
            {
                departamento = departamentoDao.findTbDepartamento( this.cod_departamento );
                preparar_objecto();
                departamentoDao.edit( departamento );
                actualizar();
                limpar();
                JOptionPane.showMessageDialog( null, "Dados alterados com sucesso!..." );

            }
            catch ( Exception e )
            {
                JOptionPane.showMessageDialog( null, "Erro ao alterar a departamento ", "ERRO", JOptionPane.ERROR_MESSAGE );
            }
        }
        else
        {
            JOptionPane.showMessageDialog( null, "Pf. seleccione a departamento para alterar" );
        }

    }

    public void limpar()
    {
        txtDescricao.setText( "" );
    }

