                setDadosEntradas();
                entradaTesourariaDao.create(entradaTesouraria);
                new MetodosUtil().adicionar_saldo_banco(entradaTesouraria.getValor(), getIdBanco(), conexao  );
                JOptionPane.showMessageDialog(null, "Entrada efectuada com sucesso!...");
