   public void busca_permissao() {
               
        try {
             // TODO add your handling code here
             setStatusUsuario( (Vector) itemPermissaoDao.getAllPermissoesByIdUsuarioAndModulo( this.idUser ,  DVML.MODULO_RH_TESOURARIA)    );
         } catch (Exception ex) {
              ex.printStackTrace();
         }
        
     }
    
  public void setStatusUsuario(Vector<TbItemPermissao> vector) {
       
          String permissao = "";
  
          //limpa
           setPermissoes(false);
          
          for (int i = 0; i < vector.size(); i++) 
          {
  
                permissao =  vector.get(i).getIdPermissao().getDescricao();
                
                /*FICHEIROS*/
                if(permissao.equals("Saldos de Contas"))
                      jlmSaldosContas.setEnabled(true);
                else if(permissao.equals("Entrada Caixa"))
                      jlmEntradasCaixa.setEnabled(true);
                else if(permissao.equals("Saida Caixa"))
                      jlmSaidasCaixa.setEnabled(true);
                else if(permissao.equals("Deposito Bancario"))
                      jlmDepositosBancarios.setEnabled(true);
                else if(permissao.equals("Levantamento Bancario"))
                      jlmLevantamentosBancarios.setEnabled(true);
                else if(permissao.equals("Transferencias Bancarias"))
                      jlmTransferenciasBancarias.setEnabled(true);

                 /*RELATORIOS*/
                else if(permissao.equals("Extrato de Movimentos Bancarios"))
                      jlmExtratoDeMovimentosBancarios.setEnabled(true);
                else if(permissao.equals("Relatorios de Entradas no Caixa") )
                      jlmRelatoriosDeEntradasNoCaixa.setEnabled(true);
                else if (permissao.equals("Relatorios de Saidas do Caixa"))
                      jlmRelatoriosDeSaidasDoCaixa.setEnabled(true);

          }
          
   }
   
   public void setPermissoes(boolean status){
    
                      /*FICHEIROS*/
             
                      jlmSaldosContas.setEnabled(status);
                      jlmEntradasCaixa.setEnabled(status);
                      jlmSaidasCaixa.setEnabled(status);
                      jlmDepositosBancarios.setEnabled(status);
                      jlmLevantamentosBancarios.setEnabled(status);
                      jlmTransferenciasBancarias.setEnabled(status);
                      
                      /*RELATORIOS*/
             
                      jlmExtratoDeMovimentosBancarios.setEnabled(status);
                      jlmRelatoriosDeEntradasNoCaixa.setEnabled(status);
                      jlmRelatoriosDeSaidasDoCaixa.setEnabled(status);

                      
   }

