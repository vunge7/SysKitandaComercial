    public boolean campos_invalidos()
    {
        Color backGround = Color.WHITE;
        Color foreGround = Color.BLACK;
        Color caretColor = Color.BLACK;

        if ( getNome().equals( "" ) )
        {
            txtNome.setBackground( backGround );
            txtNome.setForeground( foreGround );
            txtNome.setCaretColor( caretColor );
            JOptionPane.showMessageDialog( null, "Pf. Preêncha o campo Nome: " );
            return true;
        }
        else if ( getTelefone().equals( "" ) )
        {

            txtTelefone.setBackground( backGround );
            txtTelefone.setForeground( foreGround );
            txtTelefone.setCaretColor( caretColor );
            JOptionPane.showMessageDialog( null, "Pf. Preêncha o Campo Sobre Telefone: " );
            return true;
        }
        else if ( getEmail().equals( "" ) )
        {

            txtEmail.setBackground( backGround );
            txtEmail.setForeground( foreGround );
            txtEmail.setCaretColor( caretColor );
            JOptionPane.showMessageDialog( null, "Pf. Preêncha o Campo Sobre Email: " );
            return true;

        }
        else if ( getUserName().equals( "" ) )
        {

            txtUserName.setBackground( backGround );
            txtUserName.setForeground( foreGround );
            txtUserName.setCaretColor( caretColor );
            JOptionPane.showMessageDialog( null, "Pf. Preêncha o Campo Sobre UserName: " );
            return true;

        }

        return false;
    }

    public void operacao( int operacao, String descricaoCerto, String descricaoErro )
    {
        try
        {
            String novaSenha = novaSenha();

            //int codigo_sexo, int idTipousuario, String nome ,String senha, String status, String dataNascimento, String telefone, String email, String endereco, String userName
            usuarioModelo = new UsuarioModelo( codigo, getSexo(), getIdTipoUsuario(), getNome(),
                    novaSenha,
                    getStatus(),
                    getDataNascimento(),
                    getTelefone(),
                    getEmail(),
                    getEndereco(),
                    getUserName()
            );

            TbUsuario tbUsuario = null;

            if ( operacao != 3 )
            {
                if ( !campos_invalidos() )
                {
                    tbUsuario = usuarioController.createNewUsuarioInstance( operacao, usuarioModelo );
                    System.err.println( "tbUsuario: " + tbUsuario );
                    if ( tbUsuario != null )
                    {
                        JOptionPane.showMessageDialog( null, "DADOS   " + descricaoCerto + " COM SUCESSO NA BD!..." );
                        limpar();
                    }
                    else
                    {
                        System.err.println( "#1 operacao != 3 tbUsuario: " + tbUsuario );
                        JOptionPane.showMessageDialog( null, "ERRO AO " + descricaoErro + "NA BD!...", "ERRO", JOptionPane.ERROR_MESSAGE );
                    }

                }
            }
            else
            {
                if ( usuarioController.operacao( operacao, usuarioModelo ) )
                {
                    JOptionPane.showMessageDialog( null, "DADOS   " + descricaoCerto + " COM SUCESSO NA BD!..." );
                    limpar();
                }
                else
                {
                    System.err.println( "#1 else tbUsuario: " + tbUsuario );
                    JOptionPane.showMessageDialog( null, "ERRO AO " + descricaoErro + "NA BD!...", "ERRO", JOptionPane.ERROR_MESSAGE );
                }
            }

            revelarSenha( tbUsuario.getCodigo(), novaSenha );
            adicionarUsuarios();
        }
        catch ( SQLException ex )
        {
            Logger.getLogger( UsuarioTesourariaVisao.class.getName() ).log( Level.SEVERE, null, ex );
        }

    }

