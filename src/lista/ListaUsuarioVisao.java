    public void mostrarUsuarios() {
        
        Connection connection = (Connection) conexao.conectar();
        HashMap hashMap = new HashMap();
        String relatorio = "";
        
        
        if(getStatus().equals("Todos") && ! ( getSexo().equals("Masculino") || getSexo().equals("Femenino")  ))
        {
              relatorio = "relatorios/ListarUsuario.jasper";
        }
        else if(getStatus().equals("Todos") && ( getSexo().equals("Masculino") || getSexo().equals("Femenino")  ) && (  getStatus().equals("Activo")  || getStatus().equals("Desactivo") ))
        {
            System.out.println("Cheguei Mano!...");
             hashMap.put("SEXO", getSexo());
             relatorio = "relatorios/ListarUsuarioParametroTodosSexo.jasper";
        }// ( getStatus().equals("Activo") || getStatus().equals("Desactivo")  ))
        else if ( (  getStatus().equals("Activo")  || getStatus().equals("Desactivo") ))
        {
              System.out.println("Cheguei Mano Todos Activos!...");
             hashMap.put("STATUS", getStatus());
             relatorio = "relatorios/ListarUsuarioParametroTodosStatus.jasper";
        }
//       else{ 
//        
//                hashMap.put("STATUS", getStatus());
//                hashMap.put("SEXO", getSexo());
//                relatorio = "relatorios/ListarUsuarioParametro.jasper";
//               
//        }
   
       
        File file = new File(relatorio).getAbsoluteFile();
        String obterCaminho = file.getAbsolutePath();

        try {
            JasperFillManager.fillReport(obterCaminho, hashMap, connection);

            JasperPrint jasperPrint = JasperFillManager.fillReport(obterCaminho, hashMap, connection);

            if (jasperPrint.getPages().size() >= 1) {
                JasperViewer jasperViewer = new JasperViewer(jasperPrint, false);

                jasperViewer.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Nao Existem Usuarios Para essa Listagen!...");
            }
        } catch (JRException jex) {
            jex.printStackTrace();
            //System.out.println("aqui");
            JOptionPane.showMessageDialog(null, "FALHA AO TENTAR MOSTRAR OS USUARIOS!...");
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "ERRO AO TENTAR OS USUARIOS!...");
        }
    }

    
    
    
    
