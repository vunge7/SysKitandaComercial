    public void mostrar() throws SQLException
    {

        Connection connection = ( Connection ) conexao.conectar();
        HashMap hashMap = new HashMap();

        hashMap.put( "data_inicio", dc_inicio.getDate() );
        hashMap.put( "data_fim", dc_fim.getDate() );

        String relatorio = "";
        relatorio = "relatorios/relatorio_main_acerto_stock.jasper";
        File file = new File( relatorio ).getAbsoluteFile();

        String obterCaminho = file.getAbsolutePath();

        try
        {

            JasperFillManager.fillReport( obterCaminho, hashMap, connection );
            JasperPrint jasperPrint = JasperFillManager.fillReport( obterCaminho, hashMap, connection );

            if ( jasperPrint.getPages().size() >= 1 )
            {

                JasperViewer jasperViewer = new JasperViewer( jasperPrint, false );
                jasperViewer.setVisible( true );

            }
            else
            {
                JOptionPane.showMessageDialog( null, "Nao existe registo para este intervalo de datas!..." );
            }
        }
        catch ( JRException jex )
        {
            jex.printStackTrace();
            //System.out.println("aqui");
            JOptionPane.showMessageDialog( null, "Falha ao tentar mostrar o relatório!..." );
        }
        catch ( Exception ex )
        {
            ex.printStackTrace();
            JOptionPane.showMessageDialog( null, "Erro ao tentar mostrar o relatório!..." );
        }
    }

   
